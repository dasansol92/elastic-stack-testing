package org.estf.gradle.classes;

public class ExceptionEntry {

    public String field;
    public String value;
    public String type;
    public String operator;
 
    public ExceptionEntry(String field, String value, String type, String operator) {
       this.field = field;
       this.value = value;
       this.type = type;
       this.operator = operator;
   }
 }
 